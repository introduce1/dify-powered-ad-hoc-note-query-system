SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

-- 1. 出图记录表（核心生产记录）
DROP TABLE IF EXISTS drawing_record;
CREATE TABLE drawing_record (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    project_id      VARCHAR(32) COMMENT '项目WBS节点ID',
    sub_project_id  VARCHAR(32) COMMENT '子项WBS节点ID',
    dept_id         VARCHAR(32) COMMENT '部门ID',
    designer_id     VARCHAR(32) COMMENT '设计人员ID',
    drawing_no      VARCHAR(64) COMMENT '图纸编号',
    drawing_name    VARCHAR(200) COMMENT '图纸名称',
    drawing_type    VARCHAR(32) COMMENT '图纸类型（建筑/结构/暖通/电气/给排水）',
    sheet_count     INT DEFAULT 1 COMMENT '图幅数（张）',
    drawing_scale   VARCHAR(16) COMMENT '比例（1:100等）',
    finish_date     DATE COMMENT '完成日期',
    check_status    VARCHAR(16) DEFAULT '待审' COMMENT '审核状态（待审/已审/退回）',
    INDEX idx_project (project_id),
    INDEX idx_dept (dept_id),
    INDEX idx_designer (designer_id),
    INDEX idx_finish_date (finish_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='出图记录表';

-- 2. 产值记录表（月度产值统计）
DROP TABLE IF EXISTS production_value;
CREATE TABLE production_value (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    project_id      VARCHAR(32) COMMENT '项目WBS节点ID',
    dept_id         VARCHAR(32) COMMENT '部门ID',
    record_month    VARCHAR(7) COMMENT '记录月份（2026-01）',
    plan_value      DECIMAL(18,2) DEFAULT 0 COMMENT '计划产值（万元）',
    actual_value    DECIMAL(18,2) DEFAULT 0 COMMENT '实际产值（万元）',
    completion_rate DECIMAL(5,2) DEFAULT 0 COMMENT '完成率（%）',
    create_time     DATETIME DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_project (project_id),
    INDEX idx_dept (dept_id),
    INDEX idx_month (record_month)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='产值记录表';

-- 3. 生产计划表（年度/季度计划）
DROP TABLE IF EXISTS production_plan;
CREATE TABLE production_plan (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    dept_id         VARCHAR(32) COMMENT '部门ID',
    plan_year       INT COMMENT '计划年份',
    plan_quarter    INT COMMENT '计划季度（1-4，NULL为年度计划）',
    target_value    DECIMAL(18,2) DEFAULT 0 COMMENT '目标产值（万元）',
    target_drawings INT DEFAULT 0 COMMENT '目标出图数（张）',
    target_projects INT DEFAULT 0 COMMENT '目标项目数',
    create_time     DATETIME DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_dept (dept_id),
    INDEX idx_year (plan_year)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='生产计划表';

-- 4. 工时记录表
DROP TABLE IF EXISTS work_hours;
CREATE TABLE work_hours (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    user_id         VARCHAR(32) COMMENT '用户ID',
    project_id      VARCHAR(32) COMMENT '项目WBS节点ID',
    dept_id         VARCHAR(32) COMMENT '部门ID',
    work_date       DATE COMMENT '工作日期',
    hours           DECIMAL(4,1) DEFAULT 0 COMMENT '工时（小时）',
    work_content    VARCHAR(500) COMMENT '工作内容',
    INDEX idx_user (user_id),
    INDEX idx_project (project_id),
    INDEX idx_date (work_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='工时记录表';

-- 5. 项目进度表
DROP TABLE IF EXISTS project_progress;
CREATE TABLE project_progress (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    project_id      VARCHAR(32) COMMENT '项目WBS节点ID',
    dept_id         VARCHAR(32) COMMENT '部门ID',
    stage           VARCHAR(32) COMMENT '当前阶段（方案设计/初步设计/施工图设计）',
    plan_start      DATE COMMENT '计划开始日期',
    plan_end        DATE COMMENT '计划结束日期',
    actual_start    DATE COMMENT '实际开始日期',
    actual_end      DATE COMMENT '实际结束日期',
    progress_pct    INT DEFAULT 0 COMMENT '进度百分比（0-100）',
    status          VARCHAR(16) DEFAULT '进行中' COMMENT '状态（未开始/进行中/已完成/暂停）',
    update_time     DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_project (project_id),
    INDEX idx_dept (dept_id),
    INDEX idx_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='项目进度表';

